=head1 NAME

Bundle::FormMagick - A bundle to install everything required for
FormMagick

=head1 SYNOPSIS

C<perl -MCPAN -e 'install Bundle::FormMagick'>

=head1 CONTENTS

Persistence::Object::Simple 0.47

XML::Parser 2.30

Text::Template 1.23

CGI::Persistent

Class::ParamParser 

Locale::Maketext 0.18

Locale::Country 1.02

Geography::States 1.3

Time::ParseDate 00.01_03_01

CGI::Persistent 0.21

Business::CreditCard 0.21
                        

=head1 DESCRIPTION

This bundle installs all the modules needed to run FormMagick,
as well as a bunch of modules required for the validation
functions.

